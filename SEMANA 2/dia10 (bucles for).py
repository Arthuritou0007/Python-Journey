# El bucle "for" se utiliza para decirle a la computadora que realice una instrucción X cantidad de veces.
# "for" solo se utiliza si ya sabés de antemano cuántas veces se tiene que repetir la instrucción.

for numero in range(5):
    print("Hola mundo")

# "for" es la palabra clave.
# "numero" es la variable temporal en donde Python guarda el número de vuelta actual.
# "in range(5)" crea una secuencia de números