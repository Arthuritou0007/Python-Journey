print(10 > 5)   # True
print(7 < 3)    # False
print(8 == 8)   # True
print(6 != 6)   # False

# | Símbolo | Significado                 |
# | ------- | --------------------------- |
# | `>`     | Mayor que                   |
# | `<`     | Menor que                   |
# | `>=`    | Mayor o igual que           |
# | `<=`    | Menor o igual que           |
# | `==`    | Igual a (OJO: dos signos =) |
# | `!=`    | Distinto de                 |